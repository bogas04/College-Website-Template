<h2 id="process">N.S.I.T Admission Process</h2>
<p>From year 2013 onwards admission to N.S.I.T. will be done on the basis of score obtained in J.E.E. Mains. 85% of the seats are reserved for students who have qualified class 12(C.B.S.E Board or equivalent) from Delhi. Remaining 15% seats are reserved for students from outside Delhi regions.</p>
<p>A student must have been declared eligible by C.B.S.E. to participate in the counselling process. To participate in counselling the students are required to deposit Rs.1000 in either of the following ways-:</p>
<ol>
<li>Cash can be deposited through a Challan form available at the site nsit.nic.in at any of the branch of Andhra Bank. To locate the nearest Andhra Bank use the following link http://andhrabank.in/english/BranchLocator.aspx </li>
<li> Through NEFT ( account transfer).</li>
</ol> 
<p>Online registration starts from 12.06.2013 to 03.07.2013. Online registration will be possible only after payment of Rs.1000 fee. After the payment of fee one can register on the site – nsit.nic.in</p>

<h2 id="documents">Documents Required <a href="#header"><span class="glyphicon glyphicon-circle-arrow-up" ></span></a></h2>

The following documents are required for the admission procedure-
<ol>
<li>Demand Draft/Banker’s Cheque of requisite Admission Fee in favour of <i>Director,NSIT, New Delhi</i> , payable at Delhi</li>
<li>Original receipt of Counselling Fee of Rs.1000/-paid by the candidate.</li>
<li>Printout of Application Form duly attested</li>
<li>Self-attested copy of printout of Choices filled by the candidate taken after closing of Registration</li> 
<li>Original and attested copy of both Admit Card and Rank Card of JEE (Main) 2013 (Not applicable to the nominees of Government of India).</li> 
<li>Original and attested copy of both Mark Sheet and PassingCertificate of the qualifying examination.</li>
<li>Original and attested copy of class 10th Certificate/Mark Sheet in support of Date of Birth.</li>
<li>Original and attested copy of Caste/Category certificate (for the reserved categories only).</li>
<li>Original copy of Medical fitness certificate for all (except PH category)</li>
<li> Affidavit for a gap year (if applicable).</li>
</ol>
<strong>*Students from the reserved category (SC/ST/OBC/DEFENCE) need to submit a caste certificate.</strong>

