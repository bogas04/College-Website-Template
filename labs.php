<h3>Flexible Manufacturing System (FMS) and Computer Integrated Manufacturing (CIM) Lab <a href="#header"><span class="glyphicon glyphicon-circle-arrow-up" ></span> </a></h3>
<p>This laboratory has two trainer CNC machines (Denford, U.K.), a Mitsubishi Robot having 5-degrees of freedom, a linear slide on which robot moves and other supporting equipment. Students get hands-on experience in programming and working with CNC machines, robot and linear slide for the robot, completely integrated under a control of single computer. A host computer uses information in an integrated manner to create a fully automated shop floor. Besides this, hardware type experiments with Software simulation provides unique opportunity to the students to understand the principles and design involved. Some training programmes in the area of "Operation And Programming Of CNC Machines " have also been organized for the industrial and other institution personnel during the last few years.</p>
